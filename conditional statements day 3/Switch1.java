public class Switch1 {
    public static void main(String args[]){
        char ch='a';
        switch(ch){
            case 'd':System.out.println("mango");
            break;
            case 'b':System.out.println("apple");
            break;
            case 'a':System.out.println("banana");
            break;
            default:System.out.println("wake up");
            
        }
    } 
}
